    def on_button_pressed(self, event: Button.Pressed) -> None:
        bid = event.button.id
        if bid == "btn-refresh":
            self.action_refresh()
        elif bid == "btn-write":
            self.action_write_brief()
        elif bid == "btn-grok":
            self.action_run_grok_x_brief()
        elif bid == "btn-back":
            self.app.pop_screen()


