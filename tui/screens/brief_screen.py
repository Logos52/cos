# --- tiny internal helpers (duplicated  _load_json only; surgical, no loader.py edit) ---

def _load_json(path: Path, default: dict[str, Any] | None = None) -> dict[str, Any]:
    """Load JSON with graceful fallback. Matches loader.py style."""
    if default is None:
        default = {}
    try:
        if path.exists():
            return json.loads(path.read_text(encoding="utf-8"))
    except Exception:
        pass
    return default


def _expand_path(p: str | Path) -> Path:
    return Path(p).expanduser()


def _get_tz() -> ZoneInfo:
    """Never hardcode timezone. Always read from the canonical config."""
    cfg_path = DEFAULT_COS_ROOT / "tasks-calendar" / "inputs" / "config.json"
    cfg = _load_json(cfg_path, {"timezone": "Asia/Ho_Chi_Minh"})
    tz_name = cfg.get("timezone", "Asia/Ho_Chi_Minh")
    try:
        return ZoneInfo(tz_name)
    except Exception:
        return ZoneInfo("Asia/Ho_Chi_Minh")


def _load_vault_config(cos_root: Path = DEFAULT_COS_ROOT) -> dict[str, Any]:
    """Load inbox/workbench paths etc from the overview-brief config (per PRD)."""
    path = cos_root / "overview-brief" / "inputs" / "config.json"
    default = {
        "vault_path": "~/llm-knowledge-base",
        "inbox_path": "~/llm-knowledge-base/raw/inbox",
        "workbench_path": "~/llm-knowledge-base/workbench",
        "open_questions_source": "~/cos/knowledge-base/data/open-questions.json",
        "max_journal_questions": 3,
        "ai_news_path": "~/cos/overview-brief/data/ai-news.json",
    }
    return _load_json(path, default)


def _parse_frontmatter(text: str) -> dict[str, str]:
    """Minimal YAML-ish frontmatter extractor for inbox clippings (title/source/created)."""
    fm: dict[str, str] = {}
    if not text.lstrip().startswith("---"):
        return fm
    try:
        start = text.find("---")
        end = text.find("---", start + 3)
        if end == -1:
            return fm
        block = text[start + 3 : end].strip()
        for line in block.splitlines():
            line = line.strip()
            if not line or line.startswith("#") or ":" not in line:
                continue
            k, v = [part.strip() for part in line.split(":", 1)]
            k = k.lower()
            v = v.strip().strip('"').strip("'")
            fm[k] = v
    except Exception:
        pass
    return fm


# --- vault scan logic (directly mirrors toolbox/brief.md Step 2 + PRD spec) ---

def scan_inbox(config: dict[str, Any]) -> dict[str, Any]:
    """Live scan of raw/inbox/ (read-only). Returns count + items with title/source/created/filename."""
    inbox_dir = _expand_path(config.get("inbox_path", "~/llm-knowledge-base/raw/inbox"))
    if not inbox_dir.exists() or not inbox_dir.is_dir():
        return {"count": 0, "items": [], "error": f"Inbox directory not found: {inbox_dir}"}

    items: list[dict[str, Any]] = []
    try:
        md_files = sorted(inbox_dir.glob("*.md"), key=lambda p: p.stat().st_mtime, reverse=True)
        for f in md_files:
            try:
                text = f.read_text(encoding="utf-8", errors="replace")
                fm = _parse_frontmatter(text)
                item = {
                    "title": fm.get("title", f.stem),
                    "source": fm.get("source", fm.get("author", "")),
                    "created": fm.get("created", ""),
                    "filename": f.name,
                }
                items.append(item)
            except Exception as e:
                items.append({
                    "title": f.name,
                    "source": "",
                    "created": "",
                    "filename": f.name,
                    "error": str(e),
                })
    except Exception as e:
        return {"count": 0, "items": [], "error": str(e)}
    return {"count": len(items), "items": items}


def scan_workbench(config: dict[str, Any]) -> dict[str, Any]:
    """Live scan of workbench/ (read-only). Age from mtime (no frontmatter assumed, per spec)."""
    wb_dir = _expand_path(config.get("workbench_path", "~/llm-knowledge-base/workbench"))
    if not wb_dir.exists() or not wb_dir.is_dir():
        return {"count": 0, "items": [], "error": f"Workbench directory not found: {wb_dir}"}

    now = datetime.now()
    items: list[dict[str, Any]] = []
    try:
        for f in wb_dir.glob("*.md"):
            try:
                mtime = datetime.fromtimestamp(f.stat().st_mtime)
                days_old = (now - mtime).days
                items.append({"filename": f.name, "days_old": days_old})
            except Exception:
                items.append({"filename": f.name, "days_old": 999, "error": "stat failed"})
    except Exception as e:
        return {"count": 0, "items": [], "error": str(e)}

    items.sort(key=lambda x: x.get("days_old", 999))
    return {"count": len(items), "items": items}


def get_journal_questions(cos_root: Path = DEFAULT_COS_ROOT, max_n: int | None = None) -> dict[str, Any]:
    """Filter open-questions.json for journal entries (source/path contains /journal/).
    Takes most recent N (default from config or 3).
    """
    cfg = _load_vault_config(cos_root)
    max_n = max_n or int(cfg.get("max_journal_questions", 3))
    oq_path = cos_root / "knowledge-base" / "data" / "open-questions.json"
    data = _load_json(oq_path, {"open": [], "count": 0})
    open_list = data.get("open", []) or []
    if not isinstance(open_list, list):
        open_list = []

    journal = []
    for entry in open_list:
        if not isinstance(entry, dict):
            continue
        haystack = " ".join([
            str(entry.get("source", "")),
            str(entry.get("path", "")),
            str(entry.get("question", "")),
        ])
        if "/journal/" in haystack.lower():
            journal.append(entry)

    journal.sort(key=lambda x: str(x.get("raised", "")), reverse=True)
    sample = journal[:max_n]
    return {"count": len(journal), "sample": sample}


